/**
 * @brief       UART接收中断回调函数
 * @param       huart指向UART_HandleTypeDef结构体的指针
 * @retval      无
 * @note        无
 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if(huart->Instance == UARTX)
  {
    USART6_Receive_Data();
    
    HAL_UART_Receive_IT(&huart6, Receive_Data.Receive_Data_Buf, SEND_DATA_SIZE);
  }
  
  if(huart->Instance == JY901S_UARTX)
  {
    JY901S_Judge_Data(USART_Rx_Data_JY901S[0]);
    
    HAL_UART_Receive_IT(&JY901S_UARTX_Handle, USART_Rx_Data_JY901S, 1);
  }
  
  if(huart->Instance == USART1)
  {
    i=1;
    
    if(rx[0] == 0x0f && SBUS_init == 0)
    {
      // 当识别到为0x0f开始时
      SBUS_data[0] = rx[0];
      SBUS_cnt++;
      SBUS_init=1;
    }
    
    if(SBUS_init == 1 && SBUS_cnt <= 23)
    {
      // 保存中间数据
      SBUS_data[SBUS_cnt] = rx[0];
      SBUS_cnt++;
    }
    
    if(SBUS_init == 1 && SBUS_end == 0 && rx[0] == 0x00 && SBUS_cnt == 24)
    {
      // 结束
      SBUS_data[SBUS_cnt] = rx[0];
      SBUS_end = 1;
    }
    
    HAL_UART_Receive_IT(&huart1, rx, 1);
  }
}
